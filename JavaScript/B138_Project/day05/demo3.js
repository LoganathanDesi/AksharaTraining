let i=10,j=20;

console.log('i is =',i,' j is =',j);

console.log(i++); //post increment: so first print and then increment // 10

console.log(i); // 11

console.log(++i); //pre increment: so first increment and then print // 12

console.log(j--); //post decrement: so first print and then decrement // 20

console.log(j); // 19

console.log(--j); //pre decrement: so first decrement and then print // 18