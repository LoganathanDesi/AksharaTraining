//Q11.	Write a program to convert Fahrenheit to Celsius.
let fahrenheit = 90;
let celsius = (fahrenheit - 32) * 5/9;
console.log("Temperature in Celsius:", celsius);

//Q12.	Write a program to calculate the volume of a cone.
let radius = 3;
let height = 8;
let volume = (1/3) * Math.PI * radius * radius * height;
console.log("Volume of the cone:", volume);

