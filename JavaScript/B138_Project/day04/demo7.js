//arithmetic operations
let a = 10;
let b = 20;
let c = a+b;
console.log(c); // 30

console.log(20+30); // 50

console.log(30-20); // 10

console.log(10*20); // 200

console.log(20/5); // 4

console.log(20%3); // 2 // modulus operator gives the remainder of the division

console.log(20**2); // 400 // exponentiation operator raises the first operand to the power of the second operand